#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	letter;

	letter = 'a';
	while (letter <= 'z')
	{
		putchar(letter);
		letter++;
	}
}

int	main(void)
{
	ft_print_alphabet();
	return (0);
}
